package mg.adequa.beans;

public class BilanTableauDeBord {
	private int totalProduitEnStock, compteCaisse, compteEnBanque, capital;
	
	public int getTotalProduitEnStock() {
		return totalProduitEnStock;
	}
	
	public void setTotalProduitEnStock(int totalProduitEnStock) {
		this.totalProduitEnStock = totalProduitEnStock;
	}
	
	public int getCompteCaisse() {
		return compteCaisse;
	}
	
	public void setCompteCaisse(int compteCaisse) {
		this.compteCaisse = compteCaisse;
	}
	
	public int getCompteEnBanque() {
		return compteEnBanque;
	}
	
	public void setCompteEnBanque(int compteEnBanque) {
		this.compteEnBanque = compteEnBanque;
	}
	
	public int getCapital() {
		return capital;
	}
	
	public void setCapital(int capital) {
		this.capital = capital;
	}
}
