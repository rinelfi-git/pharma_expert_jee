package mg.adequa.payloads;

public class PPersonneOng {
	private int id, ong;
	private String nom, prenom;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getOng() {
		return ong;
	}
	
	public void setOng(int ong) {
		this.ong = ong;
	}
	
	public String getNom() {
		return nom;
	}
	
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	public String getPrenom() {
		return prenom;
	}
	
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
}
